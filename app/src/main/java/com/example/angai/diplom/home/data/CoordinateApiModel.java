package com.example.angai.diplom.home.data;

public class CoordinateApiModel {

    private String x;
    private String y;

    public CoordinateApiModel(String x, String y) {
        this.x = x;
        this.y = y;
    }

    public String getX() {
        return x;
    }

    public String getY() {
        return y;
    }
}
